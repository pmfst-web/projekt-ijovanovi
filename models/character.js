class Character {
  constructor(id, character, image, splash, vision, vision_image, cons, normal, skill, burst, talents_image) {
    this.id = id;
    this.character = character;
    this.image = image;
    this.splash = splash;
    this.vision = vision;
    this.vision_image = vision_image;
    this.cons = cons;
    this.normal = normal;
    this.skill = skill;
    this.burst = burst;
    this.talents_image = talents_image;
  }
}

export default Character;
